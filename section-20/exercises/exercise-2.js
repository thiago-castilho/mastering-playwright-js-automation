// Add a new student name to the beginning of the array.
// Remove the last student name from the array.
// Alphabetize the student names within the array.

// Get hands on how to Manipulate an array of strings (add, remove, sort)

let studentNames = ["Thiago", "Carol", "Rahul"];

// Add new student at the beginning
studentNames.unshift("Lucas");
console.log(studentNames);

// Remove last student
studentNames.pop();
console.log(studentNames);

// Sort alphabetically
studentNames.sort();
console.log(studentNames);



